    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        request.getRequestDispatcher("WEB-INF/view/admin/createskill.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        UserDataDetail userDataDetail = new UserDataDetail();
        AdminService adminService = new AdminService();
        String skillName = request.getParameter("skillname");
        String description = request.getParameter("description");
        userDataDetail.putAttribute("skillName", skillName);
        userDataDetail.putAttribute("description", description);
        try {
            adminService.handleCreateSkill(userDataDetail, request);
        } catch (ServletException | IOException e) {
            e.printStackTrace();
        }
        response.sendRedirect("skills");
    }

